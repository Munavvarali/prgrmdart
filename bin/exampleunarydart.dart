void main(){
  int y=1;    //(++y)  Y value Munbe 1 add cheyyum Yenitte Y nte Value add cheyyu 1+y
  print(y);   //y=1
  print(++y); //1+y1=2 1+1=2 Y value is 2
  print(++y); //1+y2=3  1+2=3 Y value 2 ayirinnu +1 add then y=3
  print(++y); //1+y3=4  1+3=4 Y value 3 ayirinnu +1 add then y=4
  print(++y); //1+y4=5  1+4=5 Y value 4 ayirinnu +1 add then y=5
  print(y);   // Y nte value 5 ane SO Y=5 PRINT cheyyum
              //(--y)  Y value Munbe 1 less cheyyum Yenitte Y nte Value add cheyyu 1-y
  print(--y); //1-y5=4  1-5=4 Y value 5 ayirinnu -1 less then y=4
  print(--y); //1-y4=3  1-4=3 Y value 4 ayirinnu -1 less then y=3
  print(--y);
  print(--y);
}
